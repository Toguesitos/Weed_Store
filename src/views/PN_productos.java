    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearActionPerformed
        GestionProducto CLI = new GestionProducto();
        Gestion_Administrativos GAdmin = new Gestion_Administrativos();
        cls_administrativos OBJAdmin = new cls_administrativos();

        usuario = OBJAdmin.getUsuario1();
        con = OBJAdmin.getContrasena();
        cls_administrativos tipo_nivel = GAdmin.GuardarDatos(usuario, con);

        if (tipo_nivel.getTipo_usuario().equals("Administrador")) {

            Errores();
            String ID;
            String Nombre;
            String Descripcion;
            String Cantidad;
            String Categoria;
            String Precio_deC;
            String Precio_deV;

            ID = txt_id_producto.getText();
            Nombre = txt_nombre_producto.getText();
            Descripcion = txt_descripcion_producto.getText();
            Cantidad = sp_cantidad_prodcuto.getValue().toString();
            Categoria = cbx_categoria_producto.getSelectedItem().toString();
            Precio_deC = txt_precio_de_compra.getText();
            Precio_deV = txt_precio_de_venta.getText();
            if (!ID.equalsIgnoreCase("")) {
                if (!Nombre.equalsIgnoreCase("")) {
                    if (!Descripcion.equalsIgnoreCase("")) {
                        if (!Cantidad.equalsIgnoreCase("0")) {
                            if (!Categoria.equalsIgnoreCase("Seleccione el item")) {
                                if (!Precio_deC.equalsIgnoreCase("")) {
                                    if (!Precio_deV.equalsIgnoreCase("")) {

                                        //ACCIONES
                                        Objects.clsProductos objProductos = new Objects.clsProductos();
                                        objProductos.setIDProd(Integer.parseInt(ID));
                                        objProductos.setNombreProd(Nombre);
                                        objProductos.setDescripProd(Descripcion);
                                        objProductos.setCantidadProd(Integer.parseInt(Cantidad));
                                        objProductos.setCategoriaProd(Categoria);
                                        objProductos.setPrecioCompra(Double.parseDouble(Precio_deC));
                                        objProductos.setPrecioVenta(Double.parseDouble(Precio_deV));

                                        JOptionPane.showMessageDialog(null, CLI.AgregarProd(objProductos));
                                        LimpiarCampos();
                                        LlenarTabla();
                                    } else {
                                        lbl_error_preciodeventa.setVisible(true);
                                    }
                                } else {
                                    lbl_error_preciodecompra.setVisible(true);
                                }
                            } else {
                                lbl_error_categoria.setVisible(true);

                            }
                        } else {
                            lbl_error_cantidad.setVisible(true);
                        }
                    } else {
                        lbl_error_DescripcionProducto.setVisible(true);
                    }
                } else {
                    lbl_error_NombreProducto.setVisible(true);
                }
            } else {
                lbl_error_IDproducto.setVisible(true);

            }
        } else {
            JOptionPane.showMessageDialog(null, "No tiene los permisos suficientes");
            LimpiarCampos();
        }
    }//GEN-LAST:event_btn_crearActionPerformed

